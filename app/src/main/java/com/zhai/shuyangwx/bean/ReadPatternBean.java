package com.zhai.shuyangwx.bean;

/**
 * 阅读模式bean
 */
public class ReadPatternBean {
    private int size;
    private String font;
    private int buju;
    private int fontcr;
    public int getSize() {
        return size;
    }
    public void setSize(int size) {
        this.size = size;
    }
    public String getFont() {
        return font;
    }
    public void setFont(String font) {
        this.font = font;
    }
    public int getBuju() {
        return buju;
    }
    public void setBuju(int buju) {
        this.buju = buju;
    }
    public int getFontcr() {
        return fontcr;
    }
    public void setFontcr(int fontcr) {
        this.fontcr = fontcr;
    }
}
